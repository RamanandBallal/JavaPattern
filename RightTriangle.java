class RightTriangle{
public static void main(String [] args){

	int i , j ,row=6;
	
	for(i=0 ; i<row ; i++) //for row outer loop
	{
	  for(j=0 ; j<=i ; j++ ) //for column inner loop
	   {
	   System.out.print("*" + " " );				}	
	
	System.out.println();//curser move to next line
	}
}
}	